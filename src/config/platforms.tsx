// Platform Icon Component for easy use
interface PlatformIconProps {
  platform: keyof typeof platformColors;
  size?: 'sm' | 'md' | 'lg' | 'xl';
  showBackground?: boolean;
  className?: string;
}

const sizeClasses = {
  sm: 'w-4 h-4',
  md: 'w-5 h-5',
  lg: 'w-6 h-6',
  xl: 'w-8 h-8',
};

export function PlatformIcon({ platform, size = 'md', showBackground = false, className = '' }: PlatformIconProps) {
  const icon = platformIcons[platform];
  const color = platformColors[platform];

  if (showBackground) {
    const bgClass = platform === 'instagram'
      ? 'bg-gradient-to-br from-[#f09433] via-[#dc2743] to-[#bc1888]'
      : '';

    return (
      <span
        className={`inline-flex items-center justify-center rounded-lg p-1.5 text-white ${bgClass} ${className}`}
        style={platform !== 'instagram' ? { backgroundColor: color } : {}}
      >
        <span className={sizeClasses[size]}>{icon}</span>
      </span>
    );
  }

  return (
    <span
      className={`inline-flex ${sizeClasses[size]} ${className}`}
      style={{ color }}
    >
      {icon}
    </span>
  );
}

// Get chart colors array for a set of platforms
export function getChartColorsForPlatforms(platforms: (keyof typeof platformChartColors)[]): string[] {
  return platforms.map(p => platformChartColors[p]);
}

// Export type for platform keys
export type PlatformKey = keyof typeof platformColors;
